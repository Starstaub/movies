DROP TABLE movie_data;


CREATE TABLE movie_data (

    idx int,
    original_title varchar(100),
    duration int,
    release varchar(50),
    storyline varchar(5000),
    certificate varchar(50),
    movie_title varchar(100),
    title_year int,
    imdb_score float,
    number_ratings int,
    episode_count int,
    budget float,
    cum_worldwide_gross float,
    type varchar(50),
    kind varchar(50)

);
